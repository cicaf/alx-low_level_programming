#include "main.h"
/**
 * print_sign - heres where it all begins.
 * @n: its an integer.
 * Return: ought to be a negative or positive.
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	return (0);
}
